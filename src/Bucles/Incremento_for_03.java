package Bucles;
import java.util.Scanner;

public class Incremento_for_03 {
    public static void main(String[] args) {
        //En el for, declaras primero una variable, pones una condicion para la variable, y creas como va a cambiarse la variable
        for(int i=0; i<=12; i++){
            System.out.println(i);
        }
    }
}
